package ALLIMPCODES;

public class Various_table_printing {
    public static void main(String[] args) {
        for (int i=1;i<100;i++){
            System.out.println("Roll No "+i +" Present Sir");
            System.out.println();
        }


        for (int i=100;i>0;i--){
            System.out.println("My Value Is "+i +" And I am Decreasing To "+(i-1));
            System.out.println();
        }

        System.out.println("Thank You For Using Our System \n----Praful Deshmukh------");

    }
}
